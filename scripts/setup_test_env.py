def init_test_db():
    """Initialize app_test.db with admin user."""
    os.makedirs(DATA_DIR, exist_ok=True)

    try:
        import sqlite3
        import bcrypt

        h = bcrypt.hashpw(b"admin123", bcrypt.gensalt()).decode()
        conn = sqlite3.connect(DB_FILE)
        c = conn.cursor()

        # Create users table
        c.execute("""
            CREATE TABLE IF NOT EXISTS users (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                username TEXT UNIQUE NOT NULL,
                password_hash TEXT NOT NULL,
                is_admin INTEGER DEFAULT 1,
                is_active INTEGER DEFAULT 1,
                created_at TEXT DEFAULT (datetime('now')),
                updated_at TEXT DEFAULT (datetime('now'))
            )
        """)

        # Upsert admin user
        c.execute("SELECT id FROM users WHERE username='admin'")
        if c.fetchone():
            c.execute("UPDATE users SET password_hash=? WHERE username='admin'", (h,))
            print("✅ Updated admin password in app_test.db")
        else:
            c.execute("INSERT INTO users (username, password_hash) VALUES (?, ?)",
                      ("admin", h))
            print("✅ Created admin user in app_test.db")

        conn.commit()
        conn.close()
        print(f"✅ Database initialized: {DB_FILE}")
    except ImportError:
        print("⚠️ bcrypt not installed — run: pip install bcrypt")
        print("   Then manually initialize the database.")
        sys.exit(1)


def check_docker():
    """Verify Docker containers are running."""
    print("\n── Checking Docker ──")
    try:
        result = subprocess.run(
            ["docker", "ps", "--format", "{{.Names}}"],
            capture_output=True, text=True, timeout=10
        )
        containers = result.stdout.strip().split("\n")

        required = ["proxysql-test-mysql", "proxysql-test-proxysql"]
        all_ok = True
        for c in required:
            if c in containers:
                print(f"  ✅ {c} is running")
            else:
                print(f"  ❌ {c} is NOT running")
                all_ok = False

        if not all_ok:
            print("\n  Run: docker compose -f docker-compose.test.yml up -d")
            return False
        return True
    except FileNotFoundError:
        print("  ❌ Docker not found")
        return False
    except subprocess.TimeoutExpired:
        print("  ❌ Docker daemon not responding")
        return False


def verify_proxysql_users():
    """Verify ProxySQL has the remote user configured."""
    print("\n── Checking ProxySQL Users ──")
    try:
        # Check remote user
        result = subprocess.run(
            ["mysql", "-h", "127.0.0.1", "-P", "6032", "-u", "proxysql_remote",
             "-premote123", "-e", "SELECT 1 AS ok"],
            capture_output=True, text=True, timeout=10
        )
        if "ok" in result.stdout:
            print("  ✅ proxysql_remote:remote123 works")
            return True
        else:
            print("  ❌ proxysql_remote not working")
            print("  Output:", result.stdout, result.stderr)
            return False
    except FileNotFoundError:
        print("  ⚠️ mysql CLI not installed — skipping ProxySQL verification")
        return True  # Don't fail if CLI not available
    except subprocess.TimeoutExpired:
        print("  ❌ ProxySQL connection timed out")
        return False


