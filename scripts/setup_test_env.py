ROOT_DIR = os.path.dirname(SCRIPT_DIR)
ENV_FILE = os.path.join(ROOT_DIR, ".env.test")
DATA_DIR = os.path.join(ROOT_DIR, "backend", "data")
DB_FILE = os.path.join(DATA_DIR, "app_test.db")


